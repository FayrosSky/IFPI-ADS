#Operadores aritméticos

# +  Adição  ex: 2 + 3 = 5
# -  Subtração ex: 5 - 2 = 3
# *  Multiplicação ex: 3 * 4 = 12
# /  Divisão  ex: 10 / 2 = 5
# // Divisão Inteira  ex: 10 // 3 = 3
# %  Módulo  ex: 10 % 3 = 1
# ** Potência ex: 2 ** 3 = 8


#Operadores Relacionais
# ==   Igual   5 == 5 = True
# !=   Diferente 5 != 3 True
# > Maior que 5 > 3 = True
# < Menor que 3 < 5 = True
# >= Maior ou igual 5 >= 5 = True
# <= Menor ou giaul 3 <= 5 = True


#Operadores Lógicos
# and  E  True and False = False
# or   Ou True or False = True
# not  Não not True = False