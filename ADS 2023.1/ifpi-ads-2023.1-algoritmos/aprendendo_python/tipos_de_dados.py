#Inteiros
meu_inteiro = 10
print(type(meu_inteiro))

#Pontos Flutuantes
meu_float = 3.14
print(type(meu_float))

#Strings
meu_texto = "Olá, Python!"
print(type(meu_texto))

#Booleans
meu_bool = True
print(type(meu_bool))

# Regras para nomear variáveis
# Devem começar com letra ou _(underline)
# Não podem conter espaços ou caracteres especiais como @, #, !
# São case-sensitive (udade e Udade são variáveis diferentes)